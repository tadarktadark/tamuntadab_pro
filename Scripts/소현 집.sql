CREATE TABLE GANGEUISIL_GAEBYEOL(
	GAGA_ID VARCHAR2(10) PRIMARY KEY NOT NULL,
	GAGA_GACO_ID VARCHAR2(20) REFERENCES GANGEUISIL_COMM(GACO_ID) NOT NULL,
